function celsiusToFahrenheit(c){
	document.write(c+"Cº is "+(c-32*1.8)+"Fº"+"<h2></h2>");
}

function fahrenheitToCelsius(f){
	document.write(f+"Fº is "+(f/1.8+32)+"Cº"+"<h2></h2>");
}
var farenheit =100;
var celsious =100;
celsiusToFahrenheit(celsious);
fahrenheitToCelsius(farenheit);